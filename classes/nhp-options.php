 */

function setup_framework_options(){
$args = array();

//Set it to dev mode to view the class settings/info in the form - default is false
$args['dev_mode'] = false;

//google api key MUST BE DEFINED IF YOU WANT TO USE GOOGLE WEBFONTS
//$args['google_api_key'] = '***';

//Remove the default stylesheet? make sure you enqueue another one all the page will look whack!
//$args['stylesheet_override'] = true;

//Choose to disable the import/export feature
$args['show_import_export'] = false;

//Choose a custom option name for your theme options, the default is the theme name in lowercase with spaces replaced by underscores
$args['opt_name'] = 'wpm_cp';

//Custom menu icon
//$args['menu_icon'] = '';

//Custom menu title for options page - default is "Options"
$args['menu_title'] = __('Content Planner', 'nhp-opts');

//Custom Page Title for options page - default is "Options"
$args['page_title'] = __('Content Planner Options', 'nhp-opts');

//Custom page slug for options page (wp-admin/themes.php?page=***) - default is "nhp_theme_options"
$args['page_slug'] = 'nhp_theme_options';

//Custom page capability - default is set to "manage_options"
//$args['page_cap'] = 'manage_options';

//page type - "menu" (adds a top menu section) or "submenu" (adds a submenu) - default is set to "menu"
$args['page_type'] = 'submenu';

//parent menu - default is set to "themes.php" (Appearance)
//the list of available parent menus is available here: http://codex.wordpress.org/Function_Reference/add_submenu_page#Parameters
$args['page_parent'] = 'options-general.php';

//custom page location - default 100 - must be unique or will override other items
$args['page_position'] = 27;

//Custom page icon class (used to override the page icon next to heading)
//$args['page_icon'] = 'icon-themes';

//Want to disable the sections showing as a submenu in the admin? uncomment this line
$args['allow_sub_menu'] = false;

//Set the Help Sidebar for the options page - no sidebar by default										
//$args['help_sidebar'] = __('<p>This is the sidebar content, HTML is allowed.</p>', 'nhp-opts');



$sections = array();

$sections[] = array(
				'icon' => NHP_OPTIONS_URL.'img/glyphicons/glyphicons_150_check.png',
				'title' => __('List Display', 'nhp-opts'),
				'desc' => __('<p class="description">Fine-tune the Content Planner list by checking or unchecking the options below.</p>', 'nhp-opts'),
				'fields' => array(
					array(
						'id' => 'features',
						'type' => 'multi_checkbox',
						'title' => __('Information to request', 'nhp-opts'),
						'options' => array('page_summary' => 'Page summary','page_goal' => 'Page goal'),//Must provide key => value pairs for multi checkbox options
						'std' => array('page_summary' => '1', 'page_goal' => '1')//See how std has changed? you also dont need to specify opts that are 0.
						),
					array(
						'id' => 'featured_image',
						'type' => 'radio',
						'title' => __('Encourage a featured image?', 'nhp-opts'), 
						'desc' => __('This is the description field, again good for additional info.', 'nhp-opts'),
						'options' => array('1' => 'Yes','2' => 'No'),//Must provide key => value pairs for radio options
						'std' => '1'
						),																		
					)
				);
				
				
	$tabs = array();
		

	global $NHP_Options;
	$NHP_Options = new NHP_Options($sections, $args, $tabs);

}//function
add_action('init', 'setup_framework_options', 0);

